// Initially stack is declared as an empty array.
let stack = [];

// Pushing elements onto the stack.
stack.push(1);
stack.push(2);
stack.push(3);

// Popping elements from the stack.
console.log(stack)
console.log(stack.pop()); // Outputs 3
console.log(stack)
console.log(stack.pop()); // Outputs 2
console.log(stack)